# Программа для проверки решения арифметического примера
print("Решите пример: 4 * 100 - 54")
ответ_пользователя = input("Ваш ответ: ")

# Правильный ответ
правильный_ответ = 4 * 100 - 54

print(f"\nПравильный ответ: {правильный_ответ}")
print(f"Ваш ответ: {ответ_пользователя}")

# Преобразование строки в число для сравнения (если нужно)
try:
    ответ_числом = int(ответ_пользователя)
    if ответ_числом == правильный_ответ:
        print("Поздравляем! Ваш ответ правильный!")
    else:
        print("К сожалению, ваш ответ неправильный.")
except ValueError:
    print("Вы ввели нечисловое значение. Невозможно сравнить ответы.")
    
