# Программа для работы с четырьмя числами
print("Пожалуйста, введите четыре числа:")

# Запрос четырех чисел
число1 = float(input("Введите первое число: "))
число2 = float(input("Введите второе число: "))
число3 = float(input("Введите третье число: "))
число4 = float(input("Введите четвертое число: "))

# Вычисления
первая_сумма = число1 + число2
вторая_сумма = число3 + число4
результат = первая_сумма / вторая_сумма

# Вывод результата с двумя цифрами после запятой
print(f"\nПервая сумма ({число1} + {число2}) = {первая_сумма}")
print(f"Вторая сумма ({число3} + {число4}) = {вторая_сумма}")
print(f"Результат деления: {результат:.2f}")
