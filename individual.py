# Программа для вычисления периметра и диагонали прямоугольника
print("Введите стороны прямоугольника:")

# Запрос сторон прямоугольника
сторона_a = float(input("Введите длину первой стороны: "))
сторона_b = float(input("Введите длину второй стороны: "))

# Вычисление периметра
периметр = 2 * (сторона_a + сторона_b)

# Вычисление длины диагонали (по теореме Пифагора)
диагональ = (сторона_a**2 + сторона_b**2)**0.5

# Вывод результатов
print(f"\nРезультаты вычислений:")
print(f"Сторона a = {сторона_a}")
print(f"Сторона b = {сторона_b}")
print(f"Периметр прямоугольника: {периметр}")
print(f"Длина диагонали: {диагональ:.2f}")
